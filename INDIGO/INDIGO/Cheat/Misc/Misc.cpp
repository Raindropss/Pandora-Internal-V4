inline void movementfix(QAngle vOldAngles, CUserCmd* pCmd, float fOldForward, float fOldSidemove)
{
	// side/forward move correction
	float deltaView;
	float f1;
	float f2;

	if (vOldAngles.y < 0.f)
		f1 = 360.0f + vOldAngles.y;
	else
		f1 = vOldAngles.y;

	if (pCmd->viewangles.y < 0.0f)
		f2 = 360.0f + pCmd->viewangles.y;
	else
		f2 = pCmd->viewangles.y;

	if (f2 < f1)
		deltaView = abs(f2 - f1);
	else
		deltaView = 360.0f - abs(f1 - f2);

	deltaView = 360.0f - deltaView;

	pCmd->forwardmove = cos(DEG2RAD(deltaView)) * fOldForward + cos(DEG2RAD(deltaView + 90.f)) * fOldSidemove;
	pCmd->sidemove = sin(DEG2RAD(deltaView)) * fOldForward + sin(DEG2RAD(deltaView + 90.f)) * fOldSidemove;
}

void AMisc::OnCreateMove( CUserCmd* pCmd )
{
	Junk();
	Junk2();
	Junk3();

	CBaseEntity* pLocal = (CBaseEntity*)Interfaces::EntityList()->GetClientEntity(Interfaces::Engine()->GetLocalPlayer());

	if (Settings::Misc::misc_Bhop && !pLocal->IsDead())
	{
		if (pCmd->buttons & IN_JUMP && !(pLocal->GetMoveType() & MOVETYPE_LADDER))
		{

			int iFlags = pLocal->GetFlags();
			if (!(iFlags & FL_ONGROUND))
				pCmd->buttons &= ~IN_JUMP;
		}
	}

	if (Settings::Misc::misc_AutoStrafe && !pLocal->IsDead())
	{
		QAngle oldangles; Interfaces::Engine()->GetViewAngles(oldangles);
		static float move = 450;
		float s_move = move * 0.5065f;
		if (pLocal->GetMoveType() & (MOVETYPE_NOCLIP | MOVETYPE_LADDER))
			return;
		if (pCmd->buttons & (IN_FORWARD | IN_MOVERIGHT | IN_MOVELEFT | IN_BACK))
			return;

		if (pCmd->buttons & IN_JUMP || !(pLocal->GetFlags() & FL_ONGROUND))
		{
			pCmd->forwardmove = move * 0.015f;
			pCmd->sidemove += (float)(((pCmd->tick_count % 2) * 2) - 1) * s_move;

			if (pCmd->mousedx)
				pCmd->sidemove = (float)clamp(pCmd->mousedx, -1, 1) * s_move;

			static float strafe = pCmd->viewangles.y;

			float rt = pCmd->viewangles.y, rotation;
			rotation = strafe - rt;

			if (rotation < FloatNegate(Interfaces::GlobalVars()->interval_per_tick))
				pCmd->sidemove = -s_move;

			if (rotation > Interfaces::GlobalVars()->interval_per_tick)
				pCmd->sidemove = s_move;

			strafe = rt;
		}
		movementfix(oldangles, pCmd, pCmd->forwardmove, pCmd->sidemove);
	}

	ConVar* skybox = Interfaces::GetConVar()->FindVar("sv_skyname");
	ConVar* postprocess = Interfaces::GetConVar()->FindVar("mat_postprocess_enable");

	if (Settings::Misc::misc_SkyName && Settings::Misc::misc_SkyboxChanger) { if (skybox) skybox->SetValue(Settings::Misc::misc_SkyName); }

	if (Settings::Misc::misc_EPostprocess)
	{
		if (Interfaces::Engine()->IsInGame())
		{
			postprocess->SetValue(0);
		}
	}
	else
	{
		if (Interfaces::Engine()->IsInGame())
		{
			postprocess->SetValue(1);
		}
	}
}

Vector AngleVector(QAngle meme)
{
	auto sy = sin(meme.y / 180.f * static_cast<float>(3.141592654f));
	auto cy = cos(meme.y / 180.f * static_cast<float>(3.141592654f));

	auto sp = sin(meme.x / 180.f * static_cast<float>(3.141592654f));
	auto cp = cos(meme.x / 180.f* static_cast<float>(3.141592654f));

	return Vector(cp*cy, cp*sy, -sp);
}

